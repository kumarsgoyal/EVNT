const mongoose = require('./db');
const Schema = mongoose.Schema;


var User = Schema({
    first_name: {
        type: String,
        required: true
    },
    last_name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});

module.exports = UserModel = mongoose.model("User", User)


